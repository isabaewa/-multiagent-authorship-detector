"""
Модуль определения задач для системы определения авторства.

Содержит две последовательные задачи CrewAI:
1. style_analysis_task — анализ стиля по трём эссе студента
2. authorship_task — сравнение профиля с проверяемым текстом

Вторая задача использует контекст (результат) первой задачи,
что обеспечивает передачу стилистического профиля между агентами.
"""

from crewai import Task
from .crew_agents import style_analyzer, authorship_detector

# ---------------------------------------------------------------------------
# Задача 1: Анализ стиля письма
# ---------------------------------------------------------------------------
# Агент получает три эссе известного автора и создаёт
# подробный стилистический профиль, который затем используется
# детектором авторства для сравнения.
# ---------------------------------------------------------------------------

style_analysis_task = Task(
    description="""
Проанализируй стиль письма студента на основе следующих эссе.

Эссе 1:
{essay1}

Эссе 2:
{essay2}

Эссе 3:
{essay3}

Создай подробный стилистический профиль, включающий:
1. Лексические особенности — предпочитаемые слова, фразы, переходы
2. Длина и сложность предложений — средняя длина, разнообразие, типы структур
3. Тон и регистр — формальный/неформальный, академический/разговорный, личный/безличный
4. Повторяющиеся грамматические привычки — пунктуация, артикли, структура придаточных
5. Стиль аргументации — как выдвигаются и обосновываются тезисы
6. Индивидуальные стилистические особенности — уникальные паттерны, обороты, ошибки
""",
    agent=style_analyzer,
    expected_output=(
        "Подробный стилистический профиль автора, охватывающий "
        "лексику, структуру предложений, тон, грамматику и "
        "стиль аргументации."
    ),
)

# ---------------------------------------------------------------------------
# Задача 2: Определение авторства
# ---------------------------------------------------------------------------
# Агент получает стилистический профиль из задачи 1 (через параметр context)
# и проверяемый текст. Результат — оценка вероятности авторства
# с развёрнутым обоснованием.
# ---------------------------------------------------------------------------

authorship_task = Task(
    description="""
Используя стилистический профиль из предыдущего анализа, сравни его со следующим проверяемым текстом.

Проверяемый текст:
{suspicious_text}

Определи, вероятно ли, что этот текст написал тот же студент.
Предоставь:
1. Оценку вероятности авторства (в процентах)
2. Подробное сравнение совпадающих и различающихся стилистических признаков
3. Ключевые аргументы за и против гипотезы об авторстве
4. Общий вывод
""",
    agent=authorship_detector,
    expected_output=(
        "Оценка вероятности авторства с развёрнутым обоснованием, "
        "включая совпадающие/различающиеся признаки и общий вывод."
    ),
    # Явная передача контекста: результат анализа стиля
    # автоматически подставляется в описание этой задачи
    context=[style_analysis_task],
)